import SwiftUI

extension Color {
    static let darkColor = Color("DarkColour")
    static let lightColor = Color("LightColour")
    static let darkGrey = Color("PrimaryGrey")
    static let lightGry = Color("LightGrey")
}


extension UIColor {
    static let darkColor = UIColor(named: "DarkColour")
    static let lightColor = UIColor(named: "LightColour")
    static let darkGrey = UIColor(named: "PrimaryGrey")
    static let lightGry = UIColor(named: "LightGrey")
}
